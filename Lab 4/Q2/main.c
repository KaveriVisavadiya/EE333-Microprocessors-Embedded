void update_display() {
	char buffer[16];
	sprintf(buffer, "%02d:%02d:%02d", hours, minutes, seconds);
	lcd16x2_i2c_setCursor(0, 0);
	lcd16x2_i2c_printf(buffer);

	if (alarm_active) {
	    lcd16x2_i2c_setCursor(1, 0);
	    lcd16x2_i2c_printf("ALARM!         ");
	    return;
	}


	if (mode == 0){  // normal mode
		if (alarm_set)
			sprintf(buffer, "Alarm:%02d:%02d ON", alarm_hours, alarm_minutes);
		else {
			sprintf(buffer, "Alarm: OFF     ");
		}
		lcd16x2_i2c_setCursor(1, 0);
		lcd16x2_i2c_printf(buffer);
	}

	else if (mode == 1) { // time setting mode
	// display cursor under hours/minutes
		if (cursor_position == 1){
			lcd16x2_i2c_setCursor(1, 0);
			lcd16x2_i2c_printf("    ^          "); // Cursor under minutes
		}
		else {
			lcd16x2_i2c_setCursor(1, 0);
			lcd16x2_i2c_printf(" ^             "); // Cursor under hours
		}
	}

	else { // alarm setting mode
		if (cursor_position == 1){
			sprintf(buffer, "Alarm:%02d:%02dM", alarm_hours, alarm_minutes);
			lcd16x2_i2c_setCursor(1, 0);
			lcd16x2_i2c_printf(buffer);
		}
		else {
			sprintf(buffer, "Alarm:%02d:%02dH", alarm_hours, alarm_minutes);
			lcd16x2_i2c_setCursor(1, 0);
			lcd16x2_i2c_printf(buffer);
		}
	}
}

void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim) {
      if (htim->Instance == TIM2) {
    	  // increment time
          seconds++;
          if (seconds == 60) {
              seconds = 0;
              minutes++;
              if (minutes == 60) {
                  minutes = 0;
                  hours = (hours + 1) % 24;
              }

          }
          // turn on alarm
          if (alarm_set && hours == alarm_hours && minutes == alarm_minutes && seconds == 0) {
              alarm_active = 1;
          }
          update_display();
      }
}

void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin) {
    uint32_t currentTick = HAL_GetTick(); // returns time in ms
    if ((currentTick - lastPressTick) < DEBOUNCE_DELAY) {
        return;
    }
    lastPressTick = currentTick;

    if (alarm_active) { //else if the alarm is active, cancel it and return to mode 0
        alarm_active = 0;
        mode = 0;
        update_display();
        return;
    }

     if (GPIO_Pin == Mode_Pin) { // mode button pressed
         mode = (mode + 1) % 3;
     }

     else if (GPIO_Pin == Cursor_Pin) {  // cursor button pressed
          if (mode == 2) { // if alarm setting, then set/reset alarm
        	  if (alarm_set == 0) alarm_set = 1;
        	  else alarm_set = 0;
          }
          else { // else cursor button is used to change ursor position
        	  cursor_position ^= 1;
          }
     }

     else if (GPIO_Pin == Increment_Pin) { // increment button pressed
          if (mode == 1) {   // time setting
        	  if (cursor_position == 0) { // cursor on hour
        		  hours = (hours + 1) % 24;
        	  }
        	  else { // cursor on minute
        		  minutes = (minutes + 1) % 60;
        		  if (minutes == 0) {
        			  hours = (hours + 1) % 24;
        		  }
        	  }
          }
          else if (mode == 2) { // alarm setting
        	  if (cursor_position == 0) {
        		  alarm_hours = (alarm_hours + 1) % 24;
        	  }
        	  else {
        		  alarm_minutes = (alarm_minutes + 1) % 60;
        		  if (alarm_minutes == 0) {
        			  alarm_hours = (alarm_hours + 1) % 24;
        		  }
        	  }
          }
     }
     update_display();
}

